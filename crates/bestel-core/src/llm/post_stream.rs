    fn drain_deltas(rx: &mut mpsc::UnboundedReceiver<LlmDelta>) -> Vec<LlmDelta> {
        let mut out = Vec::new();
        while let Ok(d) = rx.try_recv() {
            out.push(d);
        }
        out
    }

